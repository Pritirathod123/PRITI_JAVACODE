class Switch1{
	public static void main (String [] args){
		int num1 = 45;
		int num2 = 28;
		if(num1%2==0){
			System.out.println(num1 + " Even no ");
		}
		else{
			System.out.println(num1 + " Odd no");
		}
		if(num2%2==0){
			System.out.println( num2 + "Even no");
		}
		else{
			System.out.println(num2 + " Odd no ");
	}
}
}
