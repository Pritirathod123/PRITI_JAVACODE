class Switch7{
        public static void main(String [] args){
                int budget = 10000;
                switch(budget){
                        case 15000:
                                System.out.println("Jammu and Kashmir");
                                break;
                        case 10000:
                                System.out.println("Manali");
                                break;
                        case 6000:
                                System.out.println("Amrithsar");
                                break;
                        case 2000:
                                System.out.println("Mahabaleshwar");
                                 break;
                           default:
                                System.out.println("Other Destination");
                }

        }
}
