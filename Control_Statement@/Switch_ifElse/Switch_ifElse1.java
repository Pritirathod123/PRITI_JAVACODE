class Switch2{
	public static void main(String[] args){
	      char grade = 'A';
	      switch(grade){

		      case 'A':
			System.out.println("Outstanding");
			break;
	               case 'O':
		
			System.out.println( "Excellent");
	               default:
			System.out.println(" In valid state");
			break;
	      }
	
	}
}
